package com.geek.po;

public class KindModel {
	private String kindID;
	private String kind;
	private int type;

	public String getKindID() {
		return kindID;
	}

	public void setKindID(String kindID) {
		this.kindID = kindID;
	}

	public String getKind() {
		return kind;
	}

	public void setKind(String kind) {
		this.kind = kind;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}
	
	

}
